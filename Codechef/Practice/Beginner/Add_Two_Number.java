import java.io.*;
import java.util.*;


public class Main {
	public static void main(String[] args) {
	    Scanner sc = new Scanner(System.in);
	    
	    long testcases = sc.nextLong();
	    
	    while(testcases>0){
	        long a = sc.nextLong();
	        long b = sc.nextLong();
	        
	        System.out.println(a+b);
	        
	        testcases--;
	    }
	    
	}
}
